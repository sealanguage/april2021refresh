print(10 + 3)     # add
print(10 / 3)     # divide
print(10 // 3)    # floor division
print(10 ** 3)    # exponential

x = 10
x = x + 3    # increment x by 3
x += 3       # augmented assignment operator does same as above, increment x by 3
print(x)

x = 10
x -= 3       # augmented assignment decrement x by 3
print(x)